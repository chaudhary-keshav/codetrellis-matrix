"""
CodeTrellis Auto-Generated Skills (A5.5)
=========================================

Generates AI-ready skills from the CodeTrellis Matrix. Each skill is a
pre-packaged context bundle that gives AI tools the knowledge needed to
perform specific tasks (e.g., "add-endpoint", "fix-issue", "add-model").

Skills are generated by analyzing the matrix sections to detect:
  - Available API patterns → "add-endpoint" skill
  - Database models → "add-model" skill
  - Test patterns → "add-test" skill
  - Error patterns → "fix-issue" skill
  - Component patterns → "add-component" skill
  - Configuration patterns → "update-config" skill

Each skill includes:
  - Name and description
  - Trigger condition (when the skill should be suggested)
  - Required context sections from the matrix
  - Pre-built instructions for the AI

Version: 1.0.0
Created: 20 February 2026
"""

import re
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Set


@dataclass
class Skill:
    """A generated skill that can be invoked by AI tools."""
    name: str
    description: str
    trigger: str
    context_sections: List[str]
    instructions: str
    category: str = "general"
    priority: int = 0  # Lower = higher priority

    def to_dict(self) -> Dict[str, any]:
        """Convert to dictionary for serialization."""
        return {
            "name": self.name,
            "description": self.description,
            "trigger": self.trigger,
            "context_sections": self.context_sections,
            "instructions": self.instructions,
            "category": self.category,
            "priority": self.priority,
        }

    def get_context(self, sections: Dict[str, str]) -> str:
        """
        Get the pre-loaded context for this skill from matrix sections.

        Args:
            sections: Parsed matrix sections

        Returns:
            Concatenated relevant section content
        """
        parts = [f"# Skill: {self.name}", f"# {self.description}", ""]
        for section_name in self.context_sections:
            if section_name in sections:
                parts.append(sections[section_name])
                parts.append("")
        parts.append(f"# Instructions:\n{self.instructions}")
        return "\n".join(parts)


