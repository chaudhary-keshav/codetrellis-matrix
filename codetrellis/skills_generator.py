class SkillsGenerator:
    """
    Generates skills from the CodeTrellis Matrix.

    Analyzes which matrix sections are present and generates appropriate
    skills that AI tools can use for context-aware task completion.
    """

    def __init__(self, sections: Dict[str, str]) -> None:
        """
        Initialize the skills generator.

        Args:
            sections: Parsed matrix sections {name: content}
        """
        self._sections = sections
        self._available_section_names: Set[str] = set(
            k for k in sections.keys() if k != "_PREAMBLE"
        )

    def generate(self) -> List[Skill]:
        """
        Generate skills based on available matrix sections.

        Returns:
            List of generated skills, sorted by priority
        """
        skills: List[Skill] = []

        for template in SKILL_TEMPLATES:
            # Check if any of the detection sections are present
            detect_sections = template["detect_sections"]
            has_content = any(
                sname in self._available_section_names
                for sname in detect_sections
            )

            if not has_content:
                continue

            # Filter context sections to only include available ones
            available_context = [
                s for s in template["context_sections"]
                if s in self._available_section_names
            ]

            # Enrich instructions with project-specific info
            instructions = self._enrich_instructions(template)

            skill = Skill(
                name=template["name"],
                description=template["description"],
                trigger=template["trigger"],
                context_sections=available_context,
                instructions=instructions,
                category=template.get("category", "general"),
                priority=template.get("priority", 50),
            )
            skills.append(skill)

        # Sort by priority (lower = higher priority)
        skills.sort(key=lambda s: s.priority)
        return skills

    def _enrich_instructions(self, template: Dict[str, any]) -> str:
        """
        Enrich skill instructions with project-specific context.

        Appends detected patterns from the matrix to make instructions
        more specific to the actual project.
        """
        base = template["instructions"]
        enrichments: List[str] = []

        # Detect project type
        if "PROJECT" in self._sections:
            project_content = self._sections["PROJECT"]
            type_match = re.search(r'type=(.+)', project_content)
            if type_match:
                enrichments.append(f"Project type: {type_match.group(1).strip()}")
            stack_match = re.search(r'stack=(.+)', project_content)
            if stack_match:
                enrichments.append(f"Tech stack: {stack_match.group(1).strip()}")

        # Detect testing framework
        if "RUNBOOK" in self._sections:
            runbook = self._sections["RUNBOOK"]
            if "pytest" in runbook.lower():
                enrichments.append("Testing framework: pytest")
            elif "jest" in runbook.lower():
                enrichments.append("Testing framework: Jest")
            elif "go test" in runbook.lower():
                enrichments.append("Testing framework: go test")

        # Detect database
        if "DATABASE" in self._sections:
            db_content = self._sections["DATABASE"]
            dbs = re.findall(r'db:(\S+)', db_content)
            if dbs:
                enrichments.append(f"Databases: {', '.join(set(dbs))}")

        if enrichments:
            return f"{base}\n\nDetected context:\n" + "\n".join(f"  - {e}" for e in enrichments)
        return base

    def get_skill_by_name(self, name: str) -> Optional[Skill]:
        """
        Get a specific skill by name.

        Args:
            name: Skill name (e.g., "add-endpoint")

        Returns:
            Skill object or None if not found/applicable
        """
        skills = self.generate()
        for skill in skills:
            if skill.name == name:
                return skill
        return None

    def get_skill_context(self, skill_name: str) -> Optional[str]:
        """
        Get the full context for a skill (sections + instructions).

        Args:
            skill_name: Skill name

        Returns:
            Pre-loaded context string or None
        """
        skill = self.get_skill_by_name(skill_name)
        if skill is None:
            return None
        return skill.get_context(self._sections)

    def list_skill_names(self) -> List[str]:
        """Get list of available skill names."""
        return [s.name for s in self.generate()]

    def to_claude_skills_format(self) -> List[Dict[str, str]]:
        """
        Export skills in Claude Code .claude/skills/ format.

        Returns:
            List of skill definitions suitable for writing to
            .claude/skills/{name}.md files
        """
        skills = self.generate()
        result = []
        for skill in skills:
            content_parts = [
                f"# {skill.name}",
                "",
                f"> {skill.description}",
                "",
                f"**Trigger:** {skill.trigger}",
                "",
                "## Context Sections",
                "",
            ]
            for section_name in skill.context_sections:
                content_parts.append(f"- `{section_name}`")
            content_parts.extend([
                "",
                "## Instructions",
                "",
                skill.instructions,
            ])
            result.append({
                "name": skill.name,
                "filename": f"{skill.name}.md",
                "content": "\n".join(content_parts),
            })
        return result
